# Fonction addition 
def add(a, b):
    return a + b


# Fonction soustraction 
def subtract(a, b):
    return a - b

# Fonction minimum
def min_custom(a, b):
    if a < b:
        return a
    else:
        return b

# Fonction maximum
def max_custom(a, b):
    if a > b:
        return a
    else:
        return b